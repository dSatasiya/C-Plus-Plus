// WAP in c++ to find the sizes of different data types.

#include <iostream>
using namespace std;

int main() 
{
    int a;
    float b;
    char c;
    double d;
    cout<<"Size of integer is : "<<sizeof(a)<<endl;
    cout<<"Size of float is : "<<sizeof(b)<<endl;
    cout<<"Size of character is : "<<sizeof(c)<<endl;
    cout<<"Size of double is : "<<sizeof(d)<<endl;
    return 0;
}
